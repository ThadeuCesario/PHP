// Function declaration

function sayHello() {
    console.log('Hello!');
}

sayHello();

function sayHelloTo(name) {
    console.log(`Hello ${name}!`);
}

sayHelloTo('Thadeu');

function returnHi() {
    return 'Hi!';
}

console.log(returnHi());

function returnHiTo(name) {
    return `Hi ${name}!`;
}

console.log(returnHiTo('Thadeu'));